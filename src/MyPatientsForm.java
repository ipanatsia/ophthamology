    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (mode == -1) {
            //οταν ο χρήστης ζητήσει την εμφάνιση του ιστορικού
            //του επιλεγμένου ασθενή
            int maxelement = doctorsPatientsRecords.thePatients.size();
            int selectedElement = lstPatients.getSelectedIndex();
            if (selectedElement < 0 || selectedElement >= maxelement) {
                return;
            }
            HistoricalRecord newHistoricalRecord = new HistoricalRecord(doctorsPatientsRecords.getPatients().elementAt(selectedElement));
            parent.getMdiPanel().add(newHistoricalRecord);
            newHistoricalRecord.setVisible(true);
            this.setVisible(false);
        } else {
            //όταν ο χρήστης ακυρώση την εισαγωγή νέου ασθενούς
            //ή ακυρώσει την τροποποίηση των στοιχείων ενος ασθενή
            lstPatients.setEnabled(true);
            txtAddress.setEnabled(false);
            txtName.setEnabled(false);
            txtSurName.setEnabled(false);
            txtPhone.setEnabled(false);
            txtMobile.setEnabled(false);
            txtInsurance.setEnabled(false);
            txtBirthday.setEnabled(false);
            doctorsPatientsRecords.ArrangePatients();
            UpdatePatientList();
            lstPatients.setSelectedIndex(0);


        }

}//GEN-LAST:event_jButton3ActionPerformed

