    public void UpdatePatientList() {
        //Η λίστα με όλους τους ασθενείς. Εμφανίζεται μόνο το όνοματεπώνυμο του ασθενούς
        //σε αυτήν
        DefaultListModel mdl = new DefaultListModel();

        Vector<Patient> pt = doctorsPatientsRecords.getPatients();
        Enumeration<Patient> e = pt.elements();
        while (e.hasMoreElements()) {
            Patient r = e.nextElement();
            String nameToShow = r.getSurname() + ", " + r.getName();
            mdl.addElement(nameToShow);
        }
        lstPatients.setModel(mdl);

    }

