    private void lstPatientsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPatientsValueChanged
        //Όταν ο χρήστης επιλέξει κάποιον ασθενή απο τη λίστα
        //εμφανίζονται τα αναλυτκά προσωπικά στοιχεία του ασθενούς
        //στην οθόνη
        Patient[] pt = null;
        pt = doctorsPatientsRecords.getPatients().toArray(new Patient[doctorsPatientsRecords.getPatients().size()]);
        int r = lstPatients.getSelectedIndex();
        if (r >= 0 && r < pt.length) {
            txtSurName.setText(pt[r].getSurname());
            txtName.setText(pt[r].getName());
            txtAddress.setText(pt[r].getAddress());
            txtPhone.setText(pt[r].getTelephone());
            txtMobile.setText(pt[r].getMobile());
            txtInsurance.setText(pt[r].getInsurance());
            txtBirthday.setText(pt[r].getBirthday());
        }
    }//GEN-LAST:event_lstPatientsValueChanged

